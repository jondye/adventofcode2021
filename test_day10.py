from day10 import *
import pytest

test_input = [
    "[({(<(())[]>[[{[]{<()<>>",
    "[(()[<>])]({[<{<<[]>>(",
    "{([(<{}[<>[]}>{[]{[(<()>",
    "(((({<>}<{<{<>}{[]{[]{}",
    "[[<[([]))<([[{}[[()]]]",
    "[{[{({}]{}}([{[{{{}}([]",
    "{<[[]]>}<{[{[{[]{()[[[]",
    "[<(<(<(<{}))><([]([]()",
    "<{([([[(<>()){}]>(<<{{",
    "<{([{{}}[<[[[<>{}]]]>[]]",
]


def test_day10_1():
    assert day10_1(test_input) == 26397


def test_first_missmatch():
    assert first_missmatch("{([(<{}[<>[]}>{[]{[(<()>") == "}"
    assert first_missmatch("[[<[([]))<([[{}[[()]]]") == ")"
    assert first_missmatch("[{[{({}]{}}([{[{{{}}([]") == "]"
    assert first_missmatch("[<(<(<(<{}))><([]([]()") == ")"
    assert first_missmatch("<{([([[(<>()){}]>(<<{{") == ">"

    assert first_missmatch("[({(<(())[]>[[{[]{<()<>>") == None
